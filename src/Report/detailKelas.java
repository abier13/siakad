    private void pilihKelas(){
        try {
            String sql_select_all = "SELECT * FROM tbl_kelas ORDER BY nama_kelas ASC";
            conn = connectionConfig.configDB();
            stm = conn.createStatement();
            rst = stm.executeQuery(sql_select_all);
            
            cmbKelas.addItem("Pilih");
            while(rst.next()){
                cmbKelas.addItem(rst.getString("nama_kelas"));
            }
            
            rst.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal memuat data kelas: \n"+e.getMessage());
        }
    }
    
    private void tampilKelas(){
        DefaultTableModel modelKelas = new DefaultTableModel();
        modelKelas.addColumn("No");
        modelKelas.addColumn("Nama Siswa");
        
        tblDetailKelas.setModel(modelKelas);
        
    }
