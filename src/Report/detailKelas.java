    private void cmbKelasPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_cmbKelasPopupMenuWillBecomeInvisible
        String pilihKelas = cmbKelas.getSelectedItem().toString();
        String sql_pilih_kelas = "SELECT * FROM tbl_kelas JOIN tbl_guru ON tbl_guru.id_guru = tbl_kelas.id_guru WHERE nama_kelas = '"+pilihKelas+"'";
        try {
            conn = connectionConfig.configDB();
            stm = conn.createStatement();
            rst = stm.executeQuery(sql_pilih_kelas);
            
            while(rst.next()){
                idKelas = rst.getString("id_kelas");
                lblkelas.setText(rst.getString("nama_kelas"));
                lblWaliKelas.setText(rst.getString("nama"));
            }
            
            if(!idKelas.isEmpty()){
                DefaultTableModel modelSiswa = new DefaultTableModel();
                modelSiswa.addColumn("No");
                modelSiswa.addColumn("Nama Siswa");
                try {
                    int no = 1;
                    
                    String sql_tampil_siswa = "SELECT * FROM tbl_siswa WHERE id_kelas = '"+idKelas+"'";
                    Connection connect = connectionConfig.configDB();
                    Statement stmt = connect.createStatement();
                    ResultSet rs = stmt.executeQuery(sql_tampil_siswa);
                    
                    while(rs.next()){
                        modelSiswa.addRow(new Object[] {
                            no++,
                            rs.getString("nama")
                        });
                        ;
                    }
                    
                    tblDetailKelas.setModel(modelSiswa);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_cmbKelasPopupMenuWillBecomeInvisible

