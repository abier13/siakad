    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if(txtKelas.getText().isEmpty() || cmbWakel.getSelectedItem().toString().equals("Pilih")){
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong", "Pesan", JOptionPane.WARNING_MESSAGE);
        } else {
            if(idKelas == null){
                try {                    
                    String sql_insert = "INSERT INTO tbl_kelas (nama_kelas, id_guru) VALUES (?,?)";
                    Connection conn = connectionConfig.configDB();
                    PreparedStatement pst = conn.prepareStatement(sql_insert);
                    
                    pst.setString(1, txtKelas.getText());
                    pst.setString(2, idGuru);
                    
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Berhasil menambahkan data kelas", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                    showDataKelas();
                    emptyField();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Gagal menambahkan data kelas: "+e.getMessage());
                }
            } else {
                try {
                    String sql_update = "UPDATE tbl_kelas SET nama_kelas = ?, id_guru = ? WHERE id_kelas = ?";
                    Connection conn = connectionConfig.configDB();
                    PreparedStatement pst = conn.prepareStatement(sql_update);
                    
                    pst.setString(1, txtKelas.getText());
                    pst.setString(2, idGuru);
                    pst.setString(3, idKelas);
                    
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Berhasil mengubah data kelas", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                    showDataKelas();
                    emptyField();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Data gagal diubah, error: \n" + e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

