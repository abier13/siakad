    private void btnSimpan1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpan1ActionPerformed
        if(txtMapel.getText().isEmpty() || cmbTingkatKelas.getSelectedItem().toString().equals("Pilih") || cmbGuruMapel.getSelectedItem().toString().equals("Pili")){
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong", "Pesan", JOptionPane.WARNING_MESSAGE);
        } else {
            if(idMapel == null){
                try {                    
                    String sql_insert = "INSERT INTO tbl_mapel (mapel, tingkat_kelas, id_guru) VALUES (?,?,?)";
                    Connection conn = connectionConfig.configDB();
                    PreparedStatement pst = conn.prepareStatement(sql_insert);
                    
                    pst.setString(1, txtMapel.getText());
                    pst.setString(2, cmbTingkatKelas.getSelectedItem().toString());
                    pst.setString(3, idGuru);
                    
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Berhasil menambahkan data mapel", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                    showDataMapel();
                    emptyFieldMapel();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Gagal menambahkan data mapel: "+e.getMessage());
                }
            } else {
                try {
                    String sql_update = "UPDATE tbl_mapel SET mapel = ?, tingkat_kelas = ?, id_guru = ? WHERE id_mapel = ?";
                    Connection conn = connectionConfig.configDB();
                    PreparedStatement pst = conn.prepareStatement(sql_update);
                    
                    pst.setString(1, txtMapel.getText());
                    pst.setString(2, cmbTingkatKelas.getSelectedItem().toString());
                    pst.setString(3, idGuru);
                    pst.setString(4, idMapel);
                    
                    
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Berhasil mengubah data mapel", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                    showDataMapel();
                    emptyField();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Data gagal diubah, error: \n" + e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnSimpan1ActionPerformed

