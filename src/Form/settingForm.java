    private void showDataKelas() {
        DefaultTableModel modelKelas = new DefaultTableModel();
        modelKelas.addColumn("No");
        modelKelas.addColumn("ID Kelas");
        modelKelas.addColumn("Nama Kelas");
        modelKelas.addColumn("Wali Kelas");
        
        try {
            int no = 1;
            
            String sql_select_all = "SELECT tbl_kelas.id_kelas, tbl_kelas.nama_kelas, tbl_guru.nama FROM tbl_kelas INNER JOIN tbl_guru ON tbl_kelas.id_guru = tbl_guru.id_guru";
            
            Connection conn = (Connection) connectionConfig.configDB();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql_select_all);
            
            while (rs.next()){
                modelKelas.addRow(new Object[] {
                    no++,
                    rs.getString("id_kelas"),
                    rs.getString("nama_kelas"),
                    rs.getString("nama")
                });
                ;
            }
            
            tblKelas.setModel(modelKelas);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal Memuat Data: \n" + e.getMessage());
        }
    }
    
